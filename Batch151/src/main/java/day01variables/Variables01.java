package day01variables;

public class Variables01 {
    public static void main(String[] args) {
        String value = "Enter of Values";
        int age = 0;
        char note = 'A';
        boolean isRetired = false;
        byte noFoot = 42;
        short altitude = 24742;
        int popCountry = 85000000;
        long cell = 4567894526789675675L;
        double priceShirt = 121.983156789098765567;
        float priceShoe = 12.99F;

        System.out.println(value
                + "\n Age: " + age
                + "\n Note: " + note
                + "\n IsRetired: " + isRetired
                + "\n Foot Number: " + noFoot
                + "\n Altitude: " + altitude
                + "\n Population: " + popCountry
                + "\n Cell: " + cell
                + "\n Shirt Price: " + priceShirt
                + "\n Shoe Price: " + priceShoe);
    }
}
